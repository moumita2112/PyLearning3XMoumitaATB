# Dictionary
# Key and Value
# name : "Moumita"

# A dictionary is an unordered collection of data
# in a key-value pair format.

mou_dict1 = {
    "name": "Moumita",
    "age" : 33 ,
    "address" : "Assam"
}

print(len(mou_dict1))
print(mou_dict1["name"])
mou_dict1["name"] = "Poorav"
print(mou_dict1)
print(mou_dict1["age"])
print(mou_dict1["address"])

